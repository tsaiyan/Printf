#include "ft_printf.h"

int	ft_isdigit(int c)
{
	if (c >= '0' && c <= '9')
		return (1);
	return (0);
}

int	ft_crutch(long long n, t_struct *box)
{
	return ((!n && !box->precision && box->point) ? 1 : 0);
}
